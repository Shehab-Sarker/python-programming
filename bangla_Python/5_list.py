# append() ফাংশন। এই ফাংশন ব্যবহার করে আমরা আমাদের লিস্টে যেকোন নতুন আইটেম ঢোকাতে পারি। দেখা যাক:
b.append('new')
print(b) 
# insert() ফাংশনটা ব্যবহার করতে হবে। এই ফাংশনের ভিতর আমাদের নতুন আইটেমটা যে ইনডেক্সে আমরা বসাতে চাই সেটার নম্বর
# আর নতুন আইটেমটা বলে দিতে হবে। 
b.insert(1,'python')
print(b)
# একাধিক আইটেম একসাথে যোগ করার জন্য আমাদেরকে extend() ফাংশন ব্যবহার করতে হবে
b.extend(['okay','fine','good'])
print(b)
print('')

l=['a','b']
